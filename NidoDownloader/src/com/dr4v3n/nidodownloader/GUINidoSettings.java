    public GUINidoSettings() {
        initComponents();
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        setTitle("Ajustes");
        File file = new File(NidoStngs.CONFIG_DIR, "config.ini");
        if (file.exists()) {
            String mPath = "";
            try {
                mPath = FileUtils.readFileToString(file, "utf8");
            } catch (IOException ex) {
                Logger.getLogger(GUINidoSettings.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (!TextUtils.isEmpty(mPath)) {
                tfPath.setText(mPath);
            }
        } else {
            tfPath.setText(NidoStngs.DEFAULT_DIR);
        }
        setLocationRelativeTo(null);
        setResizable(false);
        
    }

