    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        File file = new File(NidoStngs.CONFIG_DIR, "config.ini");
        String data = tfPath.getText() + File.separator;
        try {
            FileUtils.writeStringToFile(file, data, "utf8", false);
        } catch (IOException e) {
            e.printStackTrace();
        }
        dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void chooseDir() {
        
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        jfc.setDialogTitle("Choose");
        jfc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            if (jfc.getSelectedFile().isDirectory())
                tfPath.setText(jfc.getSelectedFile().getAbsolutePath());
        }
    }
